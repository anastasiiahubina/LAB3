# Задача: Кількість ребер орієнтованого графа
# У першому рядку вхідних даних задано число N – кількість вершин у графі.
# Далі йде N рядків по N чисел – матриця суміжності графа G.
# У матриці G[i][j] = 1, якщо існує ребро від вершини i до вершини j, інакше 0.
# Потрібно порахувати кількість ребер у графі.

n = int(input())
count = 0
for _ in range(n):
    row = list(map(int, input().split()))
    count += sum(row)

print(count)