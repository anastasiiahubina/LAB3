# Задача: Список ребер неорієнтованого графа
# У першому рядку задано число N – кількість вершин.
# Далі йде матриця суміжності розміром N x N.
# Вивести список ребер графа (у форматі пар вершин, що з’єднані ребром).

n = int(input())
matrix = [list(map(int, input().split())) for _ in range(n)]

for i in range(n):
    for j in range(i + 1, n):
        if matrix[i][j] == 1:
            print(i + 1, j + 1)